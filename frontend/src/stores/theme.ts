export const useThemeStore = defineStore("theme", () => {
  const savedThemeId = ref(
    localStorage.getItem("quant-theme") || "midnight-indigo",
  );
  const currentTheme = ref<ThemePreset>(
    themePresets.find((t) => t.id === savedThemeId.value) || themePresets[0],
  );

  function applyTheme(theme: ThemePreset): void {
    const root = document.documentElement;
    for (const [key, value] of Object.entries(theme.variables)) {
      root.style.setProperty(key, value);
    }
    // Also set color-scheme for light themes
    if (theme.id === "light-frost" || theme.id === "warm-sand") {
      root.style.setProperty("color-scheme", "light");
    } else {
      root.style.setProperty("color-scheme", "dark");
    }
  }

  function setTheme(themeId: string): void {
    const theme = themePresets.find((t) => t.id === themeId);
    if (!theme) return;
    currentTheme.value = theme;
    savedThemeId.value = themeId;
    localStorage.setItem("quant-theme", themeId);
    applyTheme(theme);
  }

  // Apply saved theme on init
  applyTheme(currentTheme.value);

  return {
    currentTheme,
    setTheme,
  };
});
